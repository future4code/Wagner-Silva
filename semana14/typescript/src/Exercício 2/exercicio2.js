var numbersInfo = function (n1, n2) {
    console.log("Soma: " + (n1 + n2));
    console.log("Subtra\u00E7\u00E3o: " + (n1 - n2));
    console.log("Multiplica\u00E7\u00E3o: " + n1 * n2);
    n1 > n2 ? console.log("Maior: " + n1) : console.log("Maior: " + n2);
};
numbersInfo(1, 2);
numbersInfo(5, 2);
numbersInfo(4, 4);
